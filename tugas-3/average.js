function calculateAverageTwoNumbers(firstNumber, secondNumber) {
    let result = (firstNumber + secondNumber) / 2 
    console.log(`Hasil Rata2 rata dari angka ${firstNumber} dan ${secondNumber} adalah ${result}`)
}

calculateAverageTwoNumbers(3, 5)

function calculateAverageThreeNumbers(firstNumber, secondNumber, thirdNumber) {
    let result = (firstNumber + secondNumber + thirdNumber) / 3
    console.log(`Hasil Rata2 rata dari angka ${firstNumber}, ${secondNumber} dan ${thirdNumber} adalah ${result}`)
}

calculateAverageThreeNumbers(3, 5, 8)