// Soal 7.	Jika var angka1 = 10; dan var angka2 = 20;, buat pernyataan if-else untuk menentukan apakah angka1 lebih besar, lebih kecil, atau sama dengan angka2.

let number1 = 10;
let number2 = 20;

if (number1 > number2) {
  console.log(`${number1} is greater than ${number2}`);
} else if (number1 === number2) {
  console.log(`${number1} is equals to ${number2}`);
} else {
  console.log(`${number1} is less than ${number2}`)
}
