// Soal 9.	Jika var nilai = "B";, buat pernyataan if-else untuk menentukan apakah nilai tersebut "A", "B", "C", atau "Lainnya".

let value = "A"

if (value === "A") {
    console.log("Nilai adalah A")
} else if (value === "B") {
    console.log("Nilai adalah B")
} else if (value === "C") {
    console.log("Nilai adalah C")
} else {
    console.log("Lainnya")
}





