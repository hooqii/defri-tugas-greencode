// 1.	Buatlah program sederhana menggunakan if-else untuk memeriksa apakah sebuah angka adalah positif atau negatif. Tampilkan hasilnya ke konsol.

function oddNumbers(number) {
    if (number > 0) {
        console.log("Angka Positif")
    } else {
        console.log("Angka Negatif")
    }
}

oddNumbers(2)